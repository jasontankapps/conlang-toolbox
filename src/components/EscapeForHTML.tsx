// direct copy of react-lib-escape-text-content-for-browser with typescript additions

//'use strict';

interface lookup {
  [key: string]: string
}
var ESCAPE_LOOKUP: lookup = {
  '&': '&amp;',
  '>': '&gt;',
  '<': '&lt;',
  '"': '&quot;',
  '\'': '&#x27;'
};

var ESCAPE_REGEX = /[&><"']/g;

function escaper(match: string) {
  return ESCAPE_LOOKUP[match];
}

/**
 * Escapes text to prevent scripting attacks.
 *
 * @param {*} text Text value to escape.
 * @return {string} An escaped string.
 */
const escapeTextContentForBrowser = (text: string): string => {
	return (text).replace(ESCAPE_REGEX, escaper);
}

export default escapeTextContentForBrowser;
